# ---- 1. データ作成 ----
x <- 1:5
df <- data.frame(
  value   = x,
  squared = x^2
)

# ---- 2. 集計 ----
mean_value <- mean(df$value)
sd_value   <- sd(df$value)

# ---- 3. 結果表示（標準出力に出す） ----
cat("mean:", mean_value, "\n")
cat("sd  :", sd_value, "\n")
print(summary(df))